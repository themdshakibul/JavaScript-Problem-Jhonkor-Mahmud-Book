// 1. ধর তোর বন্ধু পরীক্ষায় বাম্বু খাইছে। এখন সে হ্যাপি কি না, সেটা একটা boolean টাইপের ভেরিয়েবল ডিক্লেয়ার করে সেটার মান রাখ। আর সেটা console-এ প্রিন্ট কর।
// Ans:
const Happy = true;
console.log("My Friend is ", Happy);

// 2. তুই নিজে একটা প্রোগ্রাম লিখে দেখ, তোর crush তোকে মেসেজ দিয়েছে কি না। ধর মেসেজ দিলে true, না দিলে false। সেটা প্রিন্ট কর console.log() দিয়ে।
