// 3. তোর প্রিয় টিম খেলায় জিতে গেল, এখন সেই খুশিতে বগল বাজাতে বাজাতে celebrating নামে একটা boolean ভেরিয়েবল ডিক্লেয়ার করে সেটার মান দে, আর আউটপুট দেখ।

// Ans:
const celebrating = true;
console.log("Means you are celebrating :", celebrating);

// 4. তোর এখন boolean টাইপ সম্পর্কে ভালো ধারণা হয়েছে। এবার একটা প্রোগ্রাম লিখ, যেখানে তুই তিনটা boolean ভেরিয়েবল ডিক্লেয়ার করবি: "isHappy", "hasMoney", আর "isHungry"। এই তিনটার মান যথাক্রমে true, false, আর true সেট করবি। তারপর আউটপুট দে console.log() দিয়ে।

// Ans:
const isHappy = true;
const hasMoney = false;
const isHungry = true;

console.log("Iam Happy :", isHappy);
console.log("Has Money :", hasMoney);
console.log("Is Hungry :", isHappy);

// 5. তোর আজকে পড়া শেষ হয়েছে কি না? পড়া শেষ হলে true, না হলে false হবে। আর সেটা রাখার জন্য isStudyDone নামে একটা boolean ভেরিয়েবল ডিক্লেয়ার কর।

// Ans:
const isStudyDone = false;

if (isStudyDone) {
  console.log("Study finished :", true);
} else {
  console.log("Study not finished :", false);
}

// 6. এখন isPhoneCharged নামে একটা boolean ভেরিয়েবল ডিক্লেয়ার কর। চার্জ শেষ হলে true, শেষ না হলে false।

// Ans:
const isPhoneCharged = true;

if (isPhoneCharged) {
  console.log("Phone is Charged :", true);
} else {
  console.log("Not Phone Charged :", false);
}

// 7. তোর কি রাতের খাওয়া শেষ? সেটা নিয়ে isDinnerDone নামে একটি boolean ভেরিয়েবল ডিক্লেয়ার কর। খাওয়া শেষ হলে কী লিখবি, আর শেষ না হলে কী লিখবি, সেটা তো জানোস অলরেডি।

// Ans:
